def main():
   
   num1 = int(input("Enter first number: "))
   num2 = int(input("Enter second number: "))

   dividend = num1 / num2
   print(f"The division of two numbers are {dividend}")
   modulus = num1 % num2
   print(f"The modulus of two numbers are {modulus}")




if __name__ == '__main__':
    main()